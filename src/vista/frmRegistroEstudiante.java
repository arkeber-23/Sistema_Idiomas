    private void bntGuardarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntGuardarEActionPerformed

        if (txtNombreE.getText().isEmpty() || txtApellidoE.getText().isEmpty() || txtCedulaE.getText().isEmpty()
                || txtEdadE.getText().isEmpty() || cmbGeneroE.getSelectedItem().toString().isEmpty()
                || txtDireccionE.getText().isEmpty() || txtTelefonoE.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor llene todos los campos..");
        } else {
            int idCurso = cmbCurso.getItemAt(cmbCurso.getSelectedIndex()).getIdCurso();
            String nombre = txtNombreE.getText();
            String apellido = txtApellidoE.getText();
            String cedula = txtCedulaE.getText();
            int edad = Integer.parseInt(txtEdadE.getText());
            String genero = (String) cmbGeneroE.getSelectedItem();
            String direccion = txtDireccionE.getText();
            String telefono = txtTelefonoE.getText();
            Estudiante est = new Estudiante(idCurso, nombre, apellido, cedula, telefono, direccion, edad, genero);
            int estu = nuevoEstu.registroEstudiante(est);
            if (estu > 0) {
                listarEstudiantes();
                lp.limpiarCamposTexto(jPanel3);
                lp.limpiarCamposTexto(jPanel4);

            }
        }


    }//GEN-LAST:event_bntGuardarEActionPerformed

    private void listarEstudiantes() {
        DefaultTableModel dtm = (DefaultTableModel) tblDatosE.getModel();
        List<Estudiante> lista = nuevoEstu.listarEstudiantes();
        dtm.setRowCount(0);
        for (Estudiante estudiente : lista) {
            Object[] fila = new Object[]{estudiente.getIdEstdiante(), estudiente.getNombre(),
                estudiente.getApellido(), estudiente.getCedula(), estudiente.getDireccion(), estudiente.getTelefono(),
                estudiente.getEdad(), estudiente.getSexo(),estudiente.getIdCurso()};
            dtm.addRow(fila);
        }
    }

