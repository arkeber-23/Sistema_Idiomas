    private void btnActualizarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarEActionPerformed

        if (id != 0) {//id con valor
            String nombre = txtNombreE.getText();
            String apellido = txtApellidoE.getText();
            String cedula = txtCedulaE.getText();
            String direccion = txtDireccionE.getText();
            String telefono = txtTelefonoE.getText();
            int edad = Integer.parseInt(txtEdadE.getText());
            int idCurso = cmbCurso.getItemAt(cmbCurso.getSelectedIndex()).getIdCurso();
            String genero = String.valueOf(cmbGeneroE.getSelectedItem());
            Estudiante update = new Estudiante(id, nombre, apellido, cedula, telefono, direccion, edad, genero);
            int r = nuevoEstu.actualizarEstudiante(update);
            if (r > 0) {
                JOptionPane.showMessageDialog(this, "Datos Actualizados..");
                listarEstudiantes();
                lp.limpiarCamposTexto(jPanel3);
                lp.limpiarCamposTexto(jPanel4);
                bntGuardarE.setEnabled(true);
                btnActualizarE.setEnabled(false);
                btnEliminarE.setEnabled(false);
                id = 0;
            }
            id = 0;
        }

    }//GEN-LAST:event_btnActualizarEActionPerformed

    private void btnEliminarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarEActionPerformed
        // TODO add your handling code here:
        if (id != 0) {
            int opc = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea eliminar '" + txtNombreE.getText() + "'?", "Eliminar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (opc == JOptionPane.YES_OPTION) {
                Estudiante eliminar = new Estudiante(id);
                nuevoEstu.eliminarEstudiante(eliminar);
                listarEstudiantes();
                lp.limpiarCamposTexto(jPanel3);
                lp.limpiarCamposTexto(jPanel4);
                id = 0;
            }

        }
    }//GEN-LAST:event_btnEliminarEActionPerformed

