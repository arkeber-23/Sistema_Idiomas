    private void listarDocentes() {
        DefaultTableModel dtm = (DefaultTableModel) tblDocentes.getModel();
        DocenteDao listarD = new DocenteDao();
        List<Docente> lista = listarD.listar();
        dtm.setRowCount(0);
        for (Docente docente : lista) {
            Object[] fila = new Object[]{docente.getIdDocente(),
                docente.getNombre(), docente.getApellido(), docente.getCedula(),
                docente.getEdad(), docente.getTelefono(), docente.getTitulo(),
                docente.getDireccion(), docente.getSexo()};
            dtm.addRow(fila);
        }
        bntActualizarD.setEnabled(false);
        btnEliminarD.setEnabled(false);
    }

