    private void validarNumeros(java.awt.event.KeyEvent evt, JTextField txt) {
        char caracter = evt.getKeyChar();

        if (Character.isLetter(caracter)) {
            getToolkit().beep();
            evt.consume();
        } else if (txt.getText().length() == 10) {
            getToolkit().beep();
            evt.consume();
        }
    }


